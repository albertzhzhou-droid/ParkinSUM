  static List<SourceAdapterSpec> get all =>
      [...medicationSources, ...foodSources];

  static SourceAdapterSpec? bySourceSystem(String sourceSystem) {
    for (final s in all) {
      if (s.sourceSystem.toLowerCase() == sourceSystem.toLowerCase()) return s;
    }
    return null;
  }

  /// Source families the architecture covers (implemented or spec-only).
  static List<String> get coveredSourceSystems =>
      all.map((s) => s.sourceSystem).toList(growable: false);
